print("******************迭代器遍历***************************")

a={[0]=1,2,[-1]=3,4,5,[5]=6}

print("******************ipairs迭代器遍历***************************")

-- ipairs
-- 从1开始，往后遍历
-- 只能找到连续索引的键值      断序后面不遍历
for i,k in ipairs(a) do
	print("ipairs:"..i.."_"..k)
end
print("******************pairs迭代器遍历***************************")
for i,v in pairs(a) do
	print("ipairs:"..i.."_"..v)
end